var = "hallo" # erstellt eine Variable mit dem Namen "var" und dem Wert "hallo".
print(var)
print(type(var)) # Gibt den Datentyp von var aus. 

var = "ciao" # Die Variable var hat nun den Wert "ciao". 
print(var)
print(type(var)) # Der Datentyp hat sich nicht geaendert.

# Ich kann var auch in einen integer umwandeln, indem ich ihr einen integer zuweise. 
var = 13
print(var)
print(type(var)) # Nun hat sich der Datentyp von var geaendert.
